def parse_options(text):
    matches = list(re.finditer(r"\(([A-D])\)", text))
    if len(matches) != 4:
        raise ValueError(f"Expected 4 options: {text}")
    question = text[: matches[0].start()].strip()
    options = []
    for idx, match in enumerate(matches):
        end = matches[idx + 1].start() if idx + 1 < len(matches) else len(text)
        options.append((match.group(1), text[match.end() : end].strip()))
    return question, options


def parse_data():
    items = []
    for line in DATA.splitlines():
        no_raw, answer, text = line.split("|", 2)
        no = int(no_raw)
        question, options = parse_options(text)
        items.append({"no": no, "answer": answer, "question": question, "options": options})
    return items


def unit_for(no):
    if no <= 15:
        return "day1.u-1"
    if no <= 30:
        return "day1.u-2"
    return "day1.u-3"


def analysis_for(item):
    answer_text = next(text for letter, text in item["options"] if letter == item["answer"])
    wrong = "、".join(f"{letter}. {text}" for letter, text in item["options"] if letter != item["answer"])
    return (
        f"本題答案為「{item['answer']}. {answer_text}」。{REASONS[item['no']]}"
        f"其他選項「{wrong}」與題幹所問的歷史脈絡、衛生規範、食材分類、菜系歸屬或烹調技法不符，"
        "作答時應先抓住題幹關鍵詞，再排除只是名稱相近但概念不一致的干擾選項。"
    )


